#include <iostream>
using namespace std;

int main() {
    int numero = 1; // Inicializamos la variable número con 1

    // Utilizamos un bucle while para imprimir los números del 1 al 20
    while (numero <= 20) {
        cout << numero << " ";
        numero++; // Incrementamos el valor de numero en cada iteración
    }

    cout << endl; // Agregamos un salto de línea al final

    return 0;
}

